    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int row = jTablePasien.getSelectedRow();
        String idDelete = jTablePasien.getValueAt(row, 0).toString();
        String sql = "DELETE FROM pasien WHERE id='"+idDelete+"'";
        
        int confrm = JOptionPane.showConfirmDialog(null, "Are you sure delete this data?", "Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);
        
        if(confrm == 0){
            try{
                st = db.con.createStatement();
                st.execute(sql);
                showData();
                JOptionPane.showMessageDialog(null, "Data deleted successfully!");
            } catch(SQLException e){
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

