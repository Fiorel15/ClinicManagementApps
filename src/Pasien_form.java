    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        try{
            int row = jTablePasien.getSelectedRow();
            String id = jTablePasien.getValueAt(row, 0).toString();
            
            pre = db.con.prepareStatement("UPDATE pasien SET nama_pasien=?, nik=?, alamat_pasien=?, tgl_lahir=?, jenis_kelamin=? WHERE id=?");
            pre.setString(1, jTextFieldNama.getText());
            pre.setString(2, jTextFieldNik.getText());
            pre.setString(3, jTextFieldTtl.getText());
            pre.setString(4, jTextFieldAlamat.getText());
            pre.setString(5, jTextFieldJk.getText());
            pre.setString(6, id);
            pre.executeUpdate();
            showData();
            JOptionPane.showMessageDialog(null, "Update Data Successfully!");
            pre.close();
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed
