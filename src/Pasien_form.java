    public void showData() {
       String sql = "SELECT * FROM pasien";
       
       try{
           PreparedStatement pre = db.con.prepareStatement(sql);
           ResultSet res = pre.executeQuery();
           DefaultTableModel modelTable = (DefaultTableModel)jTablePasien.getModel();
           while(res.next()){
                modelTable.addRow(new String[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), 
                   res.getString(5), res.getString(6)});
           }
           modelTable. fireTableDataChanged();
       } catch (SQLException ex) {
            Logger.getLogger(Pasien_form.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    public void Search(){
        DefaultTableModel modelTable = new DefaultTableModel();
        modelTable. addColumn("id");
        modelTable. addColumn("nama");
        modelTable. addColumn("nik");
        modelTable. addColumn("tgl_lahir");
        modelTable. addColumn("alamat");
        modelTable. addColumn("jenis_kelamin"); // ini nanti ditambahkan ya
        
        try{
            String sql = "SELECT * FROM pasien WHERE nama LIKE '%"+jButtonSearch.getText() + "%'";
            st = db.con.createStatement();
            res = st.executeQuery(sql);
            while(res.next()){
                modelTable.addRow(new String[]{res.getString(1), res.getString(2), res.getString(3), res.getString(4), 
                   res.getString(5), res.getString(6)});
                jTablePasien. setModel(modelTable);
            }
        } catch (Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
   
